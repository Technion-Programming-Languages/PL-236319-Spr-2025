from typing import List, Set

class Wizard:
    def __init__(self, name, expert, spells):
        self.name = name
        self.spells = spells
        self.expert = expert
      
    def average_spell_power(self):
        return sum(self.spells) / len(self.spells)

def find_best_wizard(wizards):
    best_wizard = wizards[0]
    for wizard in wizards:
        if wizard.average_spell_power() > best_wizard.average_spell_power():
            best_wizard = wizard
    return best_wizard

def secret(n):
    if n == 0:
        return 1
    else:
        return n * (n+1) * secret(n-1)

def convert_spell_list_to_spell_set(s, spells):
    for spell in spells:
        s.add(spell)
    return s

def len_of_list(strings):
    total = 0
    for s in strings:
        total += len(s)
    return total

